#ifndef LLVM_LIB_TARGET_SAYAC_MCTARGETDESC_SAYACINSTPRINTER_H
#define LLVM_LIB_TARGET_SAYAC_MCTARGETDESC_SAYACINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"
#include <cstdint>

namespace llvm {

class MCAsmInfo;
class MCOperand;

class SAYACInstPrinter : public MCInstPrinter {
public:
  SAYACInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  // Automatically generated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address,
                        const MCSubtargetInfo &STI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  static const char *getRegisterName(unsigned RegNo, unsigned AltIdx);

  // Print the given operand.
  void printOperand(const MCInst *MI, int OpNum, const MCSubtargetInfo &STI,
                    raw_ostream &O);
  static void printOperand(const MCOperand &MO, const MCAsmInfo *MAI,
                           raw_ostream &O);
  
   void printU8ImmOperand(const MCInst *MI, int OpNum,
                         const MCSubtargetInfo &STI, raw_ostream &O);

  void printU5ImmOperand(const MCInst *MI, int OpNum,
                         const MCSubtargetInfo &STI, raw_ostream &O);
  void printU5ImmOOperand(const MCInst *MI, int OpNum,
                          const MCSubtargetInfo &STI, raw_ostream &O);
  void printU10ImmWOOperand(const MCInst *MI, int OpNum,
                            const MCSubtargetInfo &STI, raw_ostream &O);
  void printU16ImmOperand(const MCInst *MI, int OpNum,
                          const MCSubtargetInfo &STI, raw_ostream &O);
  void printPCRelOperand(const MCInst *MI, uint64_t Address, int OpNum,
                         const MCSubtargetInfo &STI, raw_ostream &O);

  // Override MCInstPrinter.
  // void printRegName(raw_ostream &O, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;
          
  void printAddrModeMemSrc(const MCInst *MI, unsigned OpNum,
                            const MCSubtargetInfo &STI, raw_ostream &O);
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_SAYAC_MCTARGETDESC_SAYACINSTPRINTER_H